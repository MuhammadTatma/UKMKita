package Model.DataPengguna;

/**
 *
 * @author HP
 */
public class DataPengguna {
    //Atribute
    private String namaLengkapPengguna;
    private String tempatLahirPengguna;
    private String tglLahirPengguna;
    private String alamatPengguna;
    private String NIKPengguna;
    private String noTelponPengguna;
    private String emailPengguna;
    
    //Constructor
    
    //Method
    
    
    //Getter Setter
    public String getNamaLengkapPengguna() {
        return namaLengkapPengguna;
    }

    public void setNamaLengkapPengguna(String namaLengkapPengguna) {
        this.namaLengkapPengguna = namaLengkapPengguna;
    }

    public String getTempatLahirPengguna() {
        return tempatLahirPengguna;
    }

    public void setTempatLahirPengguna(String tempatLahirPengguna) {
        this.tempatLahirPengguna = tempatLahirPengguna;
    }

    public String getTglLahirPengguna() {
        return tglLahirPengguna;
    }

    public void setTglLahirPengguna(String tglLahirPengguna) {
        this.tglLahirPengguna = tglLahirPengguna;
    }

    public String getAlamatPengguna() {
        return alamatPengguna;
    }

    public void setAlamatPengguna(String alamatPengguna) {
        this.alamatPengguna = alamatPengguna;
    }

    public String getNIKPengguna() {
        return NIKPengguna;
    }

    public void setNIKPengguna(String NIKPengguna) {
        this.NIKPengguna = NIKPengguna;
    }

    public String getNoTelponPengguna() {
        return noTelponPengguna;
    }

    public void setNoTelponPengguna(String noTelponPengguna) {
        this.noTelponPengguna = noTelponPengguna;
    }

    public String getEmailPengguna() {
        return emailPengguna;
    }

    public void setEmailPengguna(String emailPengguna) {
        this.emailPengguna = emailPengguna;
    }
    
}
